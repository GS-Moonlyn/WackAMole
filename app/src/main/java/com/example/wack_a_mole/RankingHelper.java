package com.example.wack_a_mole;

public class RankingHelper {

    int lugar1, lugar2, lugar3;

    public RankingHelper(){

    }

    public RankingHelper(int lugar1, int lugar2, int lugar3){
        this.lugar1 = lugar1;
        this.lugar2 = lugar2;
        this.lugar3 = lugar3;
    }

    public int getLugar1() {
        return lugar1;
    }

    public void setLugar1(int lugar1) {
        this.lugar1 = lugar1;
    }

    public int getLugar2() {
        return lugar2;
    }

    public void setLugar2(int lugar2) {
        this.lugar2 = lugar2;
    }

    public int getLugar3() {
        return lugar3;
    }

    public void setLugar3(int lugar3) {
        this.lugar3 = lugar3;
    }
}


